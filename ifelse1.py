# ifelse1.py

money = 200

if money >= 300:
	print('ไปกินอาหารญี่ปุ่น')
elif money >= 50 :
	print('ไปกินอาหารร้านป้าแดง')
elif money >= 0:
	print('กลับไปกินข้าวที่บ้าน')
else:
	print('กรอกข้อมูลไม่ถูกต้อง กรุณากรอกจำนวน 0 ขึ้นไป')


print('กลับมาเรียน python ต่อ')

